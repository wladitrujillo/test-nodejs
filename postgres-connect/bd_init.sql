CREATE TABLE credit_profile  (
    age integer,
    gender varchar(10),
    income numeric(10, 2),
    credit_score integer,
    credit_history_length integer,
    number_of_existing_loans integer,
    loan_amount bigint,
    loan_tenure integer,
    existing_customer varchar(20),
    state varchar(100),
    city varchar(100),
    ltv_ratio numeric(15,5),
    employment_profile varchar(200),
    profile_score integer,
    occupation varchar(200)
);