CREATE TABLE credit_table  (
    age integer,
    gender varchar(10),
    annual_income numeric(10, 2),
    credit_score integer,
    credit_history_length integer,
    loans_count integer,
    loan_amount bigint,
    loan_tenure integer,
    is_customer varchar(20),
    state varchar(100),
    city varchar(100),
    ltv numeric(15,5),
    employment_profile varchar(200),
    profile_score integer,
    occupation varchar(200)
);